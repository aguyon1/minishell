#include "../../inc/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	const unsigned char	*char_src = (const unsigned char *) src;
	unsigned char		*char_dest;

	char_dest = (unsigned char *) dest;
	while (n--)
		*char_dest++ = *char_src++;
	return (dest);
}
