#include "../../inc/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	if (n == 0)
		return (NULL);
	else if (*(unsigned char *)s == (unsigned char)c)
		return ((void *)s);
	return (ft_memchr(++s, c, --n));
}
