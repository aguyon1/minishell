#include "../../inc/libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = -1;
	while (++i, s1[i] && s2[i] && (i < n - 1))
		if (s1[i] != s2[i])
			break ;
	return ((n != 0) * ((unsigned char)s1[i] - (unsigned char)s2[i]));
}
