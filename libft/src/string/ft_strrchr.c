#include "../../inc/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *res;

	res = 0;
	while (1)
	{
		if (*s == c)
			res = (char *)s;
		if (*s == 0)
			return (res);
		s++;
	}
}
