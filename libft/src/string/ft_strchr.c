#include "../../inc/libft.h"

char	*ft_strchr(const char *s, int c)
{
	const char	ch = c;
	register const char *rs = s;

	while (1)
	{
		if (*rs == ch)
			return ((char *)rs);
		if (*rs == '\0')
			return (NULL);
		rs++;
	}
}
